package entity;

public class Utility {
	public double utility = 0;
	
	public Utility() {
		utility = 0;
	}
	
	public Utility(double utility) {
		this.utility = utility;
	}
	
	public double getUtility() {
		return utility;
	}
	
	public void setUtility(double utility) {
		this.utility = utility;
	}
}
